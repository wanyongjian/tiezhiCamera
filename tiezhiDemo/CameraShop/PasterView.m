#import "PasterView.h"

@implementation PasterView
- (instancetype)init{
    if (self = [super init]) {
        self.backgroundColor = [UIColor grayColor];
    }
    return self;
}
- (instancetype)initWithFrame:(CGRect)frame{
    if (self = [super initWithFrame:frame]) {
        self.backgroundColor = [UIColor grayColor];
    }
    return self;
}

@end
